/* HMG SermonScribe v4 - Discussion Generator */
import { stripHtml, getKeywords, detectReferences, generateDiscussionQuestions, wordCount } from './utils.js';

export function generateAdvancedDiscussionGuide(sermon) {
  const text = stripHtml(sermon.content);
  const themes = getKeywords(text);
  const verses = detectReferences(text);
  const wc = wordCount(text);
  let guide = `# Small Group Discussion Guide: ${sermon.title}\n\n**Preacher:** ${sermon.preacher || 'Unknown'}\n**Date:** ${sermon.date || 'N/A'}\n**Duration:** ${sermon.duration || 'N/A'}\n**Word Count:** ${wc}\n**Themes:** ${themes.join(', ')}\n\n---\n\n`;
  guide += `## Ice Breaker (10 minutes)\n\n1. What was your favorite part of this week's sermon?\n2. Has anyone experienced something similar to what was shared?\n\n`;
  guide += `## Scripture Study (15 minutes)\n\n`;
  if (verses.length > 0) { verses.forEach((v, i) => { guide += `### Reading: ${v.ref}\n${v.text || 'Read this passage together.'}\n- What stands out to you from this verse?\n- How does this connect to the sermon message?\n\n`; }); } else { guide += `- What was the main scripture text for the sermon?\n- Read the passage together and discuss its context.\n\n`; }
  guide += `## Discussion Questions (20 minutes)\n\n`;
  const questions = generateDiscussionQuestions(themes, verses, sermon.title);
  questions.forEach((q, i) => { guide += `${i+1}. ${q}\n\n`; });
  guide += `## Personal Application (10 minutes)\n\n1. What is one thing God is speaking to you personally through this message?\n2. What is one action step you will take this week?\n3. How can our small group support each other in applying this?\n\n`;
  guide += `## Prayer (5 minutes)\n\n- Pray for the group members' needs\n- Pray for understanding of the Word\n- Pray for opportunities to share this week's message\n\n---\n\n*Generated by HMG SermonScribe v4*\n`;
  return guide;
}
